#include <iostream>

using namespace std;
int N, M;
int graph[50][50];
// 북쪽: 0 ,서쪽: 1 , 남쪽 : 2, 동쪽: 3

void DFS(int x,int y, int direction) {
	bool trigger = 0; //네 방향이 모두 청소가 되어있거나,
	//벽인경우를 가려내기 위해 만들어진 트리거
	int cur_direction = direction; // 현재 방향 저장
	if (graph[x][y] == 0) graph[x][y] = 2; // 청소 ^^

	for (int dir = 0; dir < 4; dir++) {
		direction = (direction + 1) % 4;
		int nx = x, ny = y;
		if (direction == 0) nx -= 1;
		else if (direction == 1) ny -= 1;
		else if (direction == 2) nx += 1;
		else if (direction == 3) ny += 1;
		if (graph[nx][ny] != 0) continue;
		DFS(nx, ny, direction);
		trigger = 1;
		break;
	}
	if (!trigger) {
		cur_direction = (cur_direction + 2) % 4;
		int nx = x, ny = y;
		while (true) {
			if (cur_direction == 0) nx -= 1;
			else if (cur_direction == 1) ny -= 1;
			else if (cur_direction == 2) nx += 1;
			else if (cur_direction == 3) ny += 1;
			if (graph[nx][ny] == 1) {
				int cnt = 0;
				for (int i = 0; i < N; i++) {
					for (int j = 0; j < M; j++) {
						if (graph[i][j] == 2) cnt++;
					}
				}
				cout << cnt << '\n';
				return;
			}
			if (graph[nx + 1][ny] == 0 || graph[nx - 1][ny] == 0 || graph[nx][ny + 1] == 0 || graph[nx][ny - 1] == 0) {
				cur_direction = (cur_direction + 2) % 4;
				DFS(nx, ny, cur_direction);
				break;
			}
		}
	}
}

void init() {
	cin.tie(0);
	ios::sync_with_stdio(0);
}

int main(void) {
	init();
	cin >> N >> M;
	int x, y, direction;
	cin >> x >> y >> direction;
	// 위에 방향처럼 내가 원하는대로 바꿔주기
	if (direction == 0) direction = 0;
	else if (direction == 1) direction = 3;
	else if (direction == 2) direction = 2;
	else if (direction == 3) direction = 1;
	/***************************************/
	for (int i = 0; i < N; i++) {
		for (int j = 0; j < M; j++) {
			cin >> graph[i][j];
		}
	}
	DFS(x,y,direction);
}
